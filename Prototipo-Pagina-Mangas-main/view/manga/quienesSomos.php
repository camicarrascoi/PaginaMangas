<h3 class="titulo-seccion">¿Quiénes somos?</h3>

<div class="FaQ">
    <p>
        <strong>KiwiMangas</strong> es una página web creada como parte de un proyecto educativo y personal con el objetivo de practicar y mejorar habilidades en desarrollo web, especialmente HTML, CSS, JavaScript, <a href="https://jikan.moe/">Api</a> (JikanAPI), y PHP con bases de datos. Para mantener un código organizado y escalable, seguimos el modelo <a href="https://developer.mozilla.org/es/docs/Glossary/MVC">MVC</a>, que nos ayuda a separar la lógica, el diseño y la estructura del sitio.
    </p>
    <p>
        Esta página permite a los usuarios explorar un catálogo de mangas con información detallada, ver estadísticas, filtrar por género, buscar títulos específicos y gestionar sesiones de usuario, donde podrás guardar tus mangas favoritos y, próximamente, escribir reseñas sobre los que más te gusten.
    </p>
    <p>
        Nuestro sitio es mantenido por un equipo de desarrolladores conformado por <a href="https://github.com/Spectro451">Spectro451</a> y <a href="https://github.com/camicarrascoi">Cami.m0on</a>, ambos estudiantes de la carrera de Programación y Análisis de Sistemas en el <a href="https://www.aiep.cl/">Instituto Profesional AIEP</a>.
    </p>
    <p>
        KiwiMangas es un proyecto creado para aplicar lo aprendido y seguir practicando desarrollo web, combinando nuestra pasión por el manga con herramientas modernas para construir una plataforma funcional, práctica y pensada para los fans.
    </p>
</div>
